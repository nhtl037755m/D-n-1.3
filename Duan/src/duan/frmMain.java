    public void NgayThang() {
        Thread clock = new Thread() {
            public void run() {
                try {
                    while (true) {
                        Calendar cal = new GregorianCalendar();
                        int second = cal.get(Calendar.SECOND);
                        int minute = cal.get(Calendar.MINUTE);
                        int hour = cal.get(Calendar.HOUR_OF_DAY);
                        String thu;
                        int dayOfWeek = cal.get(Calendar.DAY_OF_WEEK);
                        if (dayOfWeek == 1) {
                            thu = "Chủ nhật";
                        } else {
                            thu = "Thứ " + Integer.toString(dayOfWeek);
                        }
                        int dayOfMonth = cal.get(Calendar.DAY_OF_MONTH);
                        int month = cal.get(Calendar.MONTH);
                        int year = cal.get(Calendar.YEAR);
                        if (minute == 0 || minute == 1 || minute == 2 || minute == 3 || minute == 4
                                || minute == 5 || minute == 6 || minute == 7 || minute == 8 || minute == 9) {

                        }

                        if (second == 0 || second == 1 || second == 2 || second == 3 || second == 4
                                || second == 5 || second == 6 || second == 7 || second == 8 || second == 9) {
                            lblThoigian.setText(hour + ":" + minute + ":" + "0" + second);
                        } else {
                            lblThoigian.setText(hour + ":" + minute + ":" + second);
                        }
                        lblNgayThang.setText(thu + " Ngày " + dayOfMonth + " Tháng " + (month + 1) + " Năm " + year);
                        lblNgayThang.setText(thu + " Ngày " + dayOfMonth + " Tháng " + (month + 1) + " Năm " + year);
                        sleep(1000);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        };
        clock.start();
    }
